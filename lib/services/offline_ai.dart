// 离线 AI 响应生成器
// 基于本地知识库提供有理论支撑的育儿建议

import 'package:flutter/services.dart' show rootBundle;

/// 离线 AI：从 JSON 知识库 + 规则匹配生成回复
class OfflineAI {
  /// 生成回复（同步简化版，避免异步 asset 加载的复杂性）
  static String generateResponse(String userMessage, String? childContext) {
    final lower = userMessage.toLowerCase();

    // 根据关键词匹配案例库，给出结构化建议
    if (lower.contains('拖拉') || lower.contains('慢') || lower.contains('作业')) {
      return '''📚 **关于孩子写作业拖拉**（基于"积极管教"与"成长型思维"理论）

**核心原则**：
- 理解行为背后的信念（可能是畏难、缺乏兴趣、注意力问题）
- 关注解决方案而非惩罚
- 赞美过程而非结果

**分年龄段策略**：

🏃 **学龄前（4-6岁）**：
• 游戏化：用沙漏、角色扮演（\"我是老师\"）
• 短时多次：每次5-10分钟，完成后立即奖励
• 允许各种姿势：站着、跪着、趴着都行

🎒 **小学低年级（7-9岁）**：
• 番茄工作法：25分钟专注+5分钟休息
• 任务拆解：把作业分成小块，逐个完成
• 有限选择：\"先做数学还是语文？\"
• 环境管理：清理书桌干扰物

🧒 **高年级/青春期（13-15岁）**：
• 尊重自主：让孩子自己制定计划
• 培养时间管理：日历、待办清单
• 探讨拖延原因：焦虑？完美主义？
• 与老师沟通作业量是否合理

**技巧**：使用可视化计时器、即时正向反馈、避免唠叨（唠叨会增强逆反）。

（离线模式：切换至在线 AI 可获得更个性化对话）''';
    }

    if (lower.contains('情绪') || lower.contains('哭') || lower.contains('发脾气') || lower.contains('生气')) {
      return '''💙 **情绪管理**（基于"情感引导"与"依恋理论"）

**核心原则**：
- 所有情绪都是被接纳的，但行为有界限
- 先联结情感，再纠正行为
- 情绪是信号，不是问题

**当孩子情绪崩溃时**：

1️⃣ **接纳与陪伴**
   "我看到你很生气，我在这里陪着你。"
   不急于讲道理，不惩罚压制。

2️⃣ **帮助命名情绪**
   "你是觉得不公平对吗？"
   情绪词汇是情绪管理的工具。

3️⃣ **安全释放**
   允许哭、跺脚、捶枕头（不伤人不损物）
   深呼吸：\"我们一起吸气-呼气\"

4️⃣ **身体安抚**
   拥抱（如果孩子接受）、背部轻拍
   年幼的孩子可以用安抚物。

5️⃣ **事后复盘**
   等平静后（5-10分钟），回顾刚才发生了什么
   教替代行为：\"下次生气可以怎么做？\"

**家长自检**：你的情绪稳定是孩子学习的榜样。如果你也怒了，可以说：\"妈妈现在也很生气，我需要冷静5分钟。\"''';
    }

    if (lower.contains('青春期') || lower.contains('叛逆') || lower.contains('不听话') || lower.contains('顶嘴')) {
      return '''🌱 **青春期沟通**（基于"依恋理论"与"积极倾听"）

**核心原则**：
- 青春期是分离的过程，保持连接比控制重要
- 尊重独立性，民主型教养（authoritative）
- 用提问代替说教，用合作代替命令

**实用技巧**：

🎧 **积极倾听**
   - 不打断、不建议、不评判
   - \"嗯，然后呢？\"\"为什么这样想？\"
   - 复述确认：\"你的意思是...\"

🚪 **降低对抗**
   - 避免\"你应该/必须\"
   - 提供有限选择：\"周末你是想宅家还是出门？\"
   - 在非对抗时机沟通：路上、做饭时、睡前

🔁 **修复关系**
   - 冲突后主动破冰：\"刚才妈妈语气重了，对不起\"
   - 承认他们的成长：\"你已经是大孩子了\"

🏃 **保持联结**
   - 找到共同兴趣（游戏、运动、电影）
   - 每周固定\"亲子时光\"（哪怕30分钟，不谈心也行）

**记住**：青春期的\"叛逆\"其实是\"独立\"，是健康的发展任务。''';
    }

    if (lower.contains('二胎') || lower.contains('弟弟') || lower.contains('妹妹') || lower.contains('大宝')) {
      return '''👨‍👩‍👧‍👦 **二胎关系**（基于"情感引导"）

**核心原则**：
- 大宝的失落是真实的，需要特别关注
- 不是\"大的要让小的\"，而是公平与尊重
- 赋予大宝责任感，而非牺牲感

**行动清单**：

⭐ **专属时光**
   每周单独陪伴大宝1-2小时
    Fully focused，不谈小宝。

🤝 **让大宝参与**
   帮忙拿尿布、选衣服、讲故事
   赋予\"小助手\"角色。

💬 **认可情绪**
   \"我知道你现在有点难过，因为弟弟/妹妹占据了妈妈的时间。\"

⚖️ **公平但不均等**
   根据年龄分配资源（如零花钱、游戏时间）
   不要求大宝无限让步。

🎉 **放大友善行为**
   具体表扬：\"刚才你主动把玩具给妹妹，真有爱心！\"

**长期**：孩子们会学会竞争也会学会合作，耐心。''';
    }

    if (lower.contains('游戏') || lower.contains('手机') || lower.contains('电子')) {
      return '''📱 **电子设备管理**（基于"积极管教"）

**核心原则**：
- 完全禁止不现实，重点是平衡与自律
- 规则清晰，执行一致
- 家长以身作则

**实施策略**：

⏰ **时间规则**
   工作日≤1小时，周末≤2小时
   完成作业后才能使用
   使用屏幕时间管理工具自动执行。

🎯 **替代活动**
   运动、乐器、家庭游戏夜
   发现孩子其他兴趣点。

💭 **理解需求**
   游戏满足：成就感、社交、逃避压力
   了解孩子在游戏里获得了什么。

📋 **数字契约**
   家庭会议共同制定规则，签字贴墙上
   违反有明确后果（非暴力）

👨‍👩‍👧 **家长示范**
   减少自己刷手机时间，多面对面交流

**如果已上瘾**：出现 withdrawal（烦躁、撒谎、影响正常生活），需专业干预。''';
    }

    // 默认回复：温和地询问更多细节
    return '''谢谢你的提问。为了给你更有针对性的建议，可以告诉我：

• 孩子的年龄和性格特点？
• 这个问题的具体情境是怎样的？
• 你已经尝试过哪些方法？

这样我可以结合\"积极管教\"、\"情感引导\"等理论，提供更具体的思路。

（当前离线：如需深度对话，请激活在线 AI）''';
  }
}
